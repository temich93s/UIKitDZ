/// - durationMusic: Длительность песни
/// - nameImageMusic: Имя картинки месни
/// - namePathMusic: Имя пути по самой песни
struct DataMusic {
    var nameMusic: String
    var nameArtistMusic: String
    var durationMusic: String
    var nameImageMusic: String
    var namePathMusic: String
}
